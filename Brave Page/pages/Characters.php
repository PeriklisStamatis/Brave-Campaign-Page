<h4> Thadeus the rat </h4>
<p> The somewhat cowardly ratfolk with a troubled past. He has experienced a good deal of prejudice due to his race's notoriety as servants of the wizards of Baldom. With his psychic abilites he did his part in helping the players recover the sacred artifact of Thamal and escape Baldom unharmed. </p>
